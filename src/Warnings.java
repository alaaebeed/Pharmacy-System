    public void showTableData() {
        try {
            PreparedStatement ps;
            ResultSet rs;
            String query = "SELECT `code`, `cureName`,`amount` FROM `CureInfo`WHERE amount<10";
            ps = DbConnect.getconnection().prepareStatement(query);
            rs = ps.executeQuery();
            warnigTable.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }}
